package pl.edu.zut.app.parking.auth.enums;

/**
 * Enum representing different types of users in the parking system.
 */
public enum UserType {
    /**
     * Is defined for customers of the parking system.
     */
    CUSTOMER,
    /**
     * Is defined for owners of the parking system.
     */
    PARKING_OWNER,
    /**
     * Is defined for organizational personnel of the parking system.
     */
    ORGANIZATION_PERSONAL,
    /**
     * Is defined for administrators of the parking system.
     */
    ADMIN
}
